CREATE TABLE pasta (
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  title TEXT NOT NULL,
  category TEXT NOT NULL,
  price TEXT NOT NULL,
  ingredients TEXT, 
  url TEXT NOT NULL
);

CREATE TABLE gourmet (
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  title TEXT NOT NULL,
  category TEXT NOT NULL,
  ingredients TEXT, 
  url TEXT NOT NULL
);